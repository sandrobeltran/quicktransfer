import CodeInput from "components/codeInput";
import ContentSelector from "components/contentSelector.tsx";
import ContentWrapper from "components/generals/ContentWrapper";
import SectionTitle from "components/generals/SectionTitle";
import Header from "components/header";
import Head from "next/head";
import { FormEvent, useState } from "react";
import { ShareFile } from "utils/customInterfaces";
import getDate from "utils/getDate";
import handleShareFile from "utils/share/handleShareFile";
import handleShareImage from "utils/share/handleShareImage";
import handleShareText from "utils/share/handleShareText";

const initialState: ShareFile = {
  file: null,
  timeLimit: "",
  public: true,
};

export default function ShareFilePage() {
  const [shareFile, setShareFile] = useState<ShareFile>(initialState);

  const handleSubmit = (e: FormEvent<HTMLFormElement>) => {
    e.preventDefault();

    handleShareFile(shareFile);
  };

  return (
    <>
      <Head>
        <title>Share File</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header />
      <ContentWrapper>
        <div className="flex flex-col gap-4">
          <SectionTitle>Select the file</SectionTitle>
          <form
            className="flex items-center flex-col gap-6"
            onSubmit={handleSubmit}
          >
            <label
              htmlFor="image"
              className="
                px-8
                py-2 
                rounded-lg
                border-2
                border-violet-500 
                text-violet-500
                font-semibold 
                cursor-pointer
                hover:scale-110
                transition-transform
            
            "
            >
              {shareFile.file ? shareFile.file.name : "Select a file"}
              <input
                id="image"
                type="file"
                name="image"
                accept="file/*"
                className="hidden"
                value={""}
                onChange={(e) =>
                  setShareFile({
                    ...shareFile,
                    file: e.target.files![0],
                  })
                }
              />
            </label>
            <input
              type="datetime-local"
              name="timeLimit"
              value={shareFile.timeLimit}
              onChange={(e) =>
                setShareFile({
                  ...shareFile,
                  timeLimit: e.target.value,
                })
              }
            />
            <input
              type="checkbox"
              name="public"
              checked={shareFile.public}
              onChange={(e) =>
                setShareFile({
                  ...shareFile,
                  public: !shareFile.public,
                })
              }
            />
            <input
              className="
            px-8
            py-2 
            rounded-lg
            bg-violet-500 
            text-white 
            font-semibold 
            cursor-pointer
            hover:brightness-90
            transition-all
            "
              type="submit"
              value="Share"
            />
          </form>
        </div>
      </ContentWrapper>
    </>
  );
}
